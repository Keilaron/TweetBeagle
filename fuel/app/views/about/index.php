<h1>What we are</h1>

<p>Tweetbeagle is a data analysis tool that finds trending topics and conversations within a tweeter feed.  The key differentiators between twitter trends and our tool is that it allows you to drill down on trends within a specific group of tweeters or surrounding a specific topic.</p>

<p>Drilling down on a specific topic is done by only collecting tweets that contain a specific keyword or hashtag, and then indexing those tweets so a user can surf through the results.</p>

<p>Drilling down on trends within a group is done by collecting all the tweets from a twitter list you specify and manage in your twitter account.</p>

<br>

<h1>Who we are</h1>

<p>The idea of tweetbeagle came to be during the 2011 Canadian Election;  we built a website called polichat.ca that monitored all the Canadian MP candidates and calculated some basic stats on per party basis.  We also monitored the twitterverse for debate chatter and found trending topics on the subject.</p>

<p>We discovered that there is a ton of information that can be pulled from these tweets by applying the most basic of filters and allowing users to drill-down into the conversations they may be interested in.  So we at SilenceIT put together tweetbeagle in our off time and are really looking forward to public feedback.</p>
